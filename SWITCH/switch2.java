public class switch2 {    
public static void main(String[] args) {    
String food="Pizza";
switch (food){
case "Pizza":     
System.out.println("you have choosen a delicious italian dish");
break;
case "sushi":
System.out.println("you have choosen a fresh japanese dish");
break;
default:
System.out.println("you have choosen another type of food");
}
}
}

    
 
 