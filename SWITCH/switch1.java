public class switch1 {    
public static void main(String[] args) {    
String car="mercedes";
switch (car){
case "mercedes":     
System.out.println("you have choosen a luxorious car");
break;
case "Toyota":
System.out.println("you have choosen a reliable car");
break;
default:
System.out.println("you have choosen another car brand");
}
}
}

    
 
 